/*
  校园二手交易系统 Oracle 建表脚本
  说明：
  1. 原设计文档中的 user 表在 Oracle 中改名为 app_user，避免与 Oracle USER 关键字/函数冲突。
  2. 原设计文档中的 goods.condition 字段改名为 goods_condition，避免字段名歧义。
  3. MySQL 类型已转换为 Oracle 类型：
     INT -> NUMBER(11)
     VARCHAR -> VARCHAR2
     TEXT -> CLOB
     DATETIME -> TIMESTAMP
     DECIMAL(10,2) -> NUMBER(10,2)
     TINYINT -> NUMBER
     AUTO_INCREMENT -> GENERATED BY DEFAULT AS IDENTITY
  4. MySQL 的 ON UPDATE 自动更新时间改为 Oracle 触发器实现。
*/

/* =========================
   1. 用户与身份模块
   ========================= */

CREATE TABLE app_user (
    user_id NUMBER(11) GENERATED BY DEFAULT AS IDENTITY,
    username VARCHAR2(50) NOT NULL,
    password VARCHAR2(64) NOT NULL,
    nickname VARCHAR2(50) DEFAULT '新用户',
    avatar VARCHAR2(255),
    phone VARCHAR2(20),
    email VARCHAR2(100),
    role VARCHAR2(20) DEFAULT 'buyer' NOT NULL,
    status VARCHAR2(20) DEFAULT 'normal' NOT NULL,
    credit_score NUMBER(11) DEFAULT 100 NOT NULL,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at TIMESTAMP,

    CONSTRAINT pk_app_user PRIMARY KEY (user_id),
    CONSTRAINT uq_app_user_username UNIQUE (username),
    CONSTRAINT ck_app_user_role CHECK (role IN ('buyer', 'seller', 'admin')),
    CONSTRAINT ck_app_user_status CHECK (status IN ('normal', 'banned')),
    CONSTRAINT ck_app_user_credit_score CHECK (credit_score >= 0)
);

CREATE TABLE student_auth (
    auth_id NUMBER(11) GENERATED BY DEFAULT AS IDENTITY,
    user_id NUMBER(11) NOT NULL,
    student_id VARCHAR2(30) NOT NULL,
    real_name VARCHAR2(50) NOT NULL,
    college VARCHAR2(100) NOT NULL,
    auth_status VARCHAR2(20) DEFAULT 'pending' NOT NULL,
    auth_time TIMESTAMP,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT pk_student_auth PRIMARY KEY (auth_id),
    CONSTRAINT uq_student_auth_user_id UNIQUE (user_id),
    CONSTRAINT fk_student_auth_user FOREIGN KEY (user_id)
        REFERENCES app_user(user_id),
    CONSTRAINT ck_student_auth_status CHECK (auth_status IN ('pending', 'approved', 'rejected'))
);

/* =========================
   2. 商品管理模块
   ========================= */

CREATE TABLE category (
    category_id NUMBER(11) GENERATED BY DEFAULT AS IDENTITY,
    category_name VARCHAR2(50) NOT NULL,
    parent_id NUMBER(11),
    sort_order NUMBER(11) DEFAULT 0,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT pk_category PRIMARY KEY (category_id),
    CONSTRAINT fk_category_parent FOREIGN KEY (parent_id)
        REFERENCES category(category_id),
    CONSTRAINT ck_category_not_self CHECK (parent_id IS NULL OR parent_id <> category_id)
);

CREATE TABLE goods (
    goods_id NUMBER(11) GENERATED BY DEFAULT AS IDENTITY,
    seller_id NUMBER(11) NOT NULL,
    category_id NUMBER(11) NOT NULL,
    title VARCHAR2(200) NOT NULL,
    description CLOB,
    price NUMBER(10, 2) NOT NULL,
    goods_condition VARCHAR2(20) NOT NULL,
    goods_status VARCHAR2(20) DEFAULT 'pending' NOT NULL,
    view_count NUMBER(11) DEFAULT 0 NOT NULL,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at TIMESTAMP,

    CONSTRAINT pk_goods PRIMARY KEY (goods_id),
    CONSTRAINT fk_goods_seller FOREIGN KEY (seller_id)
        REFERENCES app_user(user_id),
    CONSTRAINT fk_goods_category FOREIGN KEY (category_id)
        REFERENCES category(category_id),
    CONSTRAINT ck_goods_price CHECK (price >= 0),
    CONSTRAINT ck_goods_condition CHECK (goods_condition IN ('全新', '几乎全新', '轻微使用', '明显痕迹')),
    CONSTRAINT ck_goods_status CHECK (goods_status IN ('pending', 'approved', 'rejected', 'locked', 'sold', 'offline')),
    CONSTRAINT ck_goods_view_count CHECK (view_count >= 0)
);

CREATE TABLE goods_image (
    image_id NUMBER(11) GENERATED BY DEFAULT AS IDENTITY,
    goods_id NUMBER(11) NOT NULL,
    image_url VARCHAR2(255) NOT NULL,
    sort_order NUMBER(11) DEFAULT 0,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT pk_goods_image PRIMARY KEY (image_id),
    CONSTRAINT fk_goods_image_goods FOREIGN KEY (goods_id)
        REFERENCES goods(goods_id)
);

CREATE TABLE favorite (
    favorite_id NUMBER(11) GENERATED BY DEFAULT AS IDENTITY,
    user_id NUMBER(11) NOT NULL,
    goods_id NUMBER(11) NOT NULL,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT pk_favorite PRIMARY KEY (favorite_id),
    CONSTRAINT fk_favorite_user FOREIGN KEY (user_id)
        REFERENCES app_user(user_id),
    CONSTRAINT fk_favorite_goods FOREIGN KEY (goods_id)
        REFERENCES goods(goods_id),
    CONSTRAINT uq_favorite_user_goods UNIQUE (user_id, goods_id)
);

/* =========================
   3. 议价与交易模块
   ========================= */

CREATE TABLE bargain_offer (
    offer_id NUMBER(11) GENERATED BY DEFAULT AS IDENTITY,
    goods_id NUMBER(11) NOT NULL,
    buyer_id NUMBER(11) NOT NULL,
    offer_price NUMBER(10, 2) NOT NULL,
    seller_response VARCHAR2(20) DEFAULT 'pending',
    counter_price NUMBER(10, 2),
    offer_status VARCHAR2(20) DEFAULT 'active' NOT NULL,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at TIMESTAMP,

    CONSTRAINT pk_bargain_offer PRIMARY KEY (offer_id),
    CONSTRAINT fk_bargain_offer_goods FOREIGN KEY (goods_id)
        REFERENCES goods(goods_id),
    CONSTRAINT fk_bargain_offer_buyer FOREIGN KEY (buyer_id)
        REFERENCES app_user(user_id),
    CONSTRAINT ck_bargain_offer_price CHECK (offer_price >= 0),
    CONSTRAINT ck_bargain_counter_price CHECK (counter_price IS NULL OR counter_price >= 0),
    CONSTRAINT ck_bargain_seller_response CHECK (seller_response IN ('pending', 'accepted', 'rejected', 'countered')),
    CONSTRAINT ck_bargain_offer_status CHECK (offer_status IN ('active', 'accepted', 'rejected', 'closed')),
    CONSTRAINT ck_bargain_counter_required CHECK (
        (seller_response = 'countered' AND counter_price IS NOT NULL)
        OR
        (seller_response <> 'countered')
    )
);

CREATE TABLE trade_order (
    order_id NUMBER(11) GENERATED BY DEFAULT AS IDENTITY,
    goods_id NUMBER(11) NOT NULL,
    buyer_id NUMBER(11) NOT NULL,
    seller_id NUMBER(11) NOT NULL,
    final_price NUMBER(10, 2) NOT NULL,
    order_status VARCHAR2(20) DEFAULT 'pending_meet' NOT NULL,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at TIMESTAMP,

    CONSTRAINT pk_trade_order PRIMARY KEY (order_id),
    CONSTRAINT fk_trade_order_goods FOREIGN KEY (goods_id)
        REFERENCES goods(goods_id),
    CONSTRAINT fk_trade_order_buyer FOREIGN KEY (buyer_id)
        REFERENCES app_user(user_id),
    CONSTRAINT fk_trade_order_seller FOREIGN KEY (seller_id)
        REFERENCES app_user(user_id),
    CONSTRAINT ck_trade_order_price CHECK (final_price >= 0),
    CONSTRAINT ck_trade_order_status CHECK (order_status IN ('pending_meet', 'in_meet', 'completed', 'cancelled')),
    CONSTRAINT ck_trade_order_buyer_seller CHECK (buyer_id <> seller_id)
);

CREATE TABLE appointment (
    appointment_id NUMBER(11) GENERATED BY DEFAULT AS IDENTITY,
    order_id NUMBER(11) NOT NULL,
    meet_time TIMESTAMP NOT NULL,
    meet_place VARCHAR2(255) NOT NULL,
    confirm_code VARCHAR2(10) NOT NULL,
    appointment_status VARCHAR2(20) DEFAULT 'pending' NOT NULL,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT pk_appointment PRIMARY KEY (appointment_id),
    CONSTRAINT uq_appointment_order_id UNIQUE (order_id),
    CONSTRAINT fk_appointment_order FOREIGN KEY (order_id)
        REFERENCES trade_order(order_id),
    CONSTRAINT ck_appointment_status CHECK (appointment_status IN ('pending', 'confirmed', 'completed', 'cancelled')),
    CONSTRAINT ck_appointment_confirm_code_len CHECK (LENGTH(confirm_code) BETWEEN 6 AND 10)
);

/* =========================
   4. 沟通模块
   ========================= */

CREATE TABLE chat_session (
    session_id NUMBER(11) GENERATED BY DEFAULT AS IDENTITY,
    goods_id NUMBER(11) NOT NULL,
    buyer_id NUMBER(11) NOT NULL,
    seller_id NUMBER(11) NOT NULL,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT pk_chat_session PRIMARY KEY (session_id),
    CONSTRAINT fk_chat_session_goods FOREIGN KEY (goods_id)
        REFERENCES goods(goods_id),
    CONSTRAINT fk_chat_session_buyer FOREIGN KEY (buyer_id)
        REFERENCES app_user(user_id),
    CONSTRAINT fk_chat_session_seller FOREIGN KEY (seller_id)
        REFERENCES app_user(user_id),
    CONSTRAINT uq_chat_session_goods_buyer_seller UNIQUE (goods_id, buyer_id, seller_id),
    CONSTRAINT ck_chat_session_buyer_seller CHECK (buyer_id <> seller_id)
);

CREATE TABLE chat_message (
    message_id NUMBER(11) GENERATED BY DEFAULT AS IDENTITY,
    session_id NUMBER(11) NOT NULL,
    sender_id NUMBER(11) NOT NULL,
    content CLOB NOT NULL,
    is_read NUMBER(1) DEFAULT 0 NOT NULL,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT pk_chat_message PRIMARY KEY (message_id),
    CONSTRAINT fk_chat_message_session FOREIGN KEY (session_id)
        REFERENCES chat_session(session_id),
    CONSTRAINT fk_chat_message_sender FOREIGN KEY (sender_id)
        REFERENCES app_user(user_id),
    CONSTRAINT ck_chat_message_is_read CHECK (is_read IN (0, 1))
);

/* =========================
   5. 评价与举报模块
   ========================= */

CREATE TABLE review (
    review_id NUMBER(11) GENERATED BY DEFAULT AS IDENTITY,
    order_id NUMBER(11) NOT NULL,
    reviewer_id NUMBER(11) NOT NULL,
    reviewed_user_id NUMBER(11) NOT NULL,
    rating NUMBER(1) NOT NULL,
    content CLOB,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT pk_review PRIMARY KEY (review_id),
    CONSTRAINT fk_review_order FOREIGN KEY (order_id)
        REFERENCES trade_order(order_id),
    CONSTRAINT fk_review_reviewer FOREIGN KEY (reviewer_id)
        REFERENCES app_user(user_id),
    CONSTRAINT fk_review_reviewed_user FOREIGN KEY (reviewed_user_id)
        REFERENCES app_user(user_id),
    CONSTRAINT uq_review_order_reviewer UNIQUE (order_id, reviewer_id),
    CONSTRAINT ck_review_rating CHECK (rating BETWEEN 1 AND 5),
    CONSTRAINT ck_review_users CHECK (reviewer_id <> reviewed_user_id)
);

CREATE TABLE report (
    report_id NUMBER(11) GENERATED BY DEFAULT AS IDENTITY,
    reporter_id NUMBER(11) NOT NULL,
    report_type VARCHAR2(20) NOT NULL,
    target_goods_id NUMBER(11),
    target_user_id NUMBER(11),
    target_order_id NUMBER(11),
    reason CLOB NOT NULL,
    report_status VARCHAR2(20) DEFAULT 'pending' NOT NULL,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,

    CONSTRAINT pk_report PRIMARY KEY (report_id),
    CONSTRAINT fk_report_reporter FOREIGN KEY (reporter_id)
        REFERENCES app_user(user_id),
    CONSTRAINT fk_report_target_goods FOREIGN KEY (target_goods_id)
        REFERENCES goods(goods_id),
    CONSTRAINT fk_report_target_user FOREIGN KEY (target_user_id)
        REFERENCES app_user(user_id),
    CONSTRAINT fk_report_target_order FOREIGN KEY (target_order_id)
        REFERENCES trade_order(order_id),
    CONSTRAINT ck_report_type CHECK (report_type IN ('goods', 'user', 'order')),
    CONSTRAINT ck_report_status CHECK (report_status IN ('pending', 'processing', 'resolved', 'rejected')),
    CONSTRAINT ck_report_target_by_type CHECK (
        (report_type = 'goods' AND target_goods_id IS NOT NULL AND target_user_id IS NULL AND target_order_id IS NULL)
        OR
        (report_type = 'user' AND target_goods_id IS NULL AND target_user_id IS NOT NULL AND target_order_id IS NULL)
        OR
        (report_type = 'order' AND target_goods_id IS NULL AND target_user_id IS NULL AND target_order_id IS NOT NULL)
    )
);

