    notice_id NUMBER(11) GENERATED BY DEFAULT AS IDENTITY,
    title VARCHAR2(200) NOT NULL,
    content CLOB NOT NULL,
    notice_type VARCHAR2(30) NOT NULL,
    publisher_id NUMBER(11) NOT NULL,
    created_at TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    updated_at TIMESTAMP,

    CONSTRAINT pk_notice PRIMARY KEY (notice_id),
    CONSTRAINT fk_notice_publisher FOREIGN KEY (publisher_id)
        REFERENCES app_user(user_id),
    CONSTRAINT ck_notice_type CHECK (notice_type IN ('system', 'transaction', 'violation'))
